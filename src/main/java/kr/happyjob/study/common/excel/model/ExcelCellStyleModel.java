package kr.happyjob.study.common.excel.model;

import org.apache.poi.hssf.usermodel.HSSFCellStyle;
import org.apache.poi.hssf.util.HSSFColor;
import org.apache.poi.ss.usermodel.CellStyle;
import org.apache.poi.ss.usermodel.Font;

public class ExcelCellStyleModel {

	/* 폰트 설정*/
	private String fontName;
	private short fontHeight;
	private boolean fontBlod;
	private short fontColor;
	
	/* 셀 설정 */
	private short cellAlignment;
	private short cellVerticalAlignment;
	private short cellFillForegroundColor;
	private short cellFillPattern;
	private short cellBorderLeft;
	private short cellBorderRight;
	private short cellBorderTop;
	private short cellBorderBottom;

	public ExcelCellStyleModel() {
		
		fontName = "맑은 고딕";
		fontHeight = (short)(11*20);
		fontBlod = false;
		fontColor = Font.COLOR_NORMAL;
		
		/* 셀 설정 */
		cellAlignment = HSSFCellStyle.ALIGN_CENTER;
		cellVerticalAlignment = HSSFCellStyle.VERTICAL_CENTER;
		cellFillForegroundColor = HSSFColor.WHITE.index;
		cellFillPattern = CellStyle.SOLID_FOREGROUND;
		cellBorderLeft = HSSFCellStyle.BORDER_THIN;
		cellBorderRight = HSSFCellStyle.BORDER_THIN;
		cellBorderTop = HSSFCellStyle.BORDER_THIN;
		cellBorderBottom = HSSFCellStyle.BORDER_THIN;
		
	}

	/**
	 * @return the fontName
	 */
	public String getFontName() {
		return fontName;
	}

	/**
	 * @param fontName the fontName to set
	 */
	public void setFontName(String fontName) {
		this.fontName = fontName;
	}

	/**
	 * @return the fontHeight
	 */
	public short getFontHeight() {
		return fontHeight;
	}

	/**
	 * @param fontHeight the fontHeight to set
	 */
	public void setFontHeight(short fontHeight) {
		this.fontHeight = fontHeight;
	}

	/**
	 * @return the fontBlod
	 */
	public boolean getFontBlod() {
		return fontBlod;
	}

	/**
	 * @param fontBlod the fontBlod to set
	 */
	public void setFontBlod(boolean fontBlod) {
		this.fontBlod = fontBlod;
	}

	/**
	 * @return the fontColor
	 */
	public short getFontColor() {
		return fontColor;
	}

	/**
	 * @param fontColor the fontColor to set
	 */
	public void setFontColor(short fontColor) {
		this.fontColor = fontColor;
	}

	/**
	 * @return the cellAlignment
	 */
	public short getCellAlignment() {
		return cellAlignment;
	}

	/**
	 * @param cellAlignment the cellAlignment to set
	 */
	public void setCellAlignment(short cellAlignment) {
		this.cellAlignment = cellAlignment;
	}

	/**
	 * @return the cellVerticalAlignment
	 */
	public short getCellVerticalAlignment() {
		return cellVerticalAlignment;
	}

	/**
	 * @param cellVerticalAlignment the cellVerticalAlignment to set
	 */
	public void setCellVerticalAlignment(short cellVerticalAlignment) {
		this.cellVerticalAlignment = cellVerticalAlignment;
	}

	/**
	 * @return the cellFillForegroundColor
	 */
	public short getCellFillForegroundColor() {
		return cellFillForegroundColor;
	}

	/**
	 * @param cellFillForegroundColor the cellFillForegroundColor to set
	 */
	public void setCellFillForegroundColor(short cellFillForegroundColor) {
		this.cellFillForegroundColor = cellFillForegroundColor;
	}

	/**
	 * @return the cellFillPattern
	 */
	public short getCellFillPattern() {
		return cellFillPattern;
	}

	/**
	 * @param cellFillPattern the cellFillPattern to set
	 */
	public void setCellFillPattern(short cellFillPattern) {
		this.cellFillPattern = cellFillPattern;
	}

	/**
	 * @return the cellBorderLeft
	 */
	public short getCellBorderLeft() {
		return cellBorderLeft;
	}

	/**
	 * @param cellBorderLeft the cellBorderLeft to set
	 */
	public void setCellBorderLeft(short cellBorderLeft) {
		this.cellBorderLeft = cellBorderLeft;
	}

	/**
	 * @return the cellBorderRight
	 */
	public short getCellBorderRight() {
		return cellBorderRight;
	}

	/**
	 * @param cellBorderRight the cellBorderRight to set
	 */
	public void setCellBorderRight(short cellBorderRight) {
		this.cellBorderRight = cellBorderRight;
	}

	/**
	 * @return the cellBorderTop
	 */
	public short getCellBorderTop() {
		return cellBorderTop;
	}

	/**
	 * @param cellBorderTop the cellBorderTop to set
	 */
	public void setCellBorderTop(short cellBorderTop) {
		this.cellBorderTop = cellBorderTop;
	}

	/**
	 * @return the cellBorderBottom
	 */
	public short getCellBorderBottom() {
		return cellBorderBottom;
	}

	/**
	 * @param cellBorderBottom the cellBorderBottom to set
	 */
	public void setCellBorderBottom(short cellBorderBottom) {
		this.cellBorderBottom = cellBorderBottom;
	}
}
